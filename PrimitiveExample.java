public class PrimitiveExample {
    public static void main(String[] args) {
        int number = 100;
        double decimal = 10.5;
        char letter = 'A';
        boolean isTrue = true;
        System.out.println("Number: " + number);
        System.out.println("Decimal: " + decimal);
        System.out.println("Letter: " + letter);
        System.out.println("Is True: " + isTrue);
    }
}